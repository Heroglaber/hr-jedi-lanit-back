package ru.lanit.bpm.jedu.hrjedi.model;

import javax.persistence.*;

@Entity
@Table(name = "OFFICE")
public class Office {
    @Id
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "office_id_generator")
    @SequenceGenerator(name = "office_id_generator", sequenceName = "sq_office_id", allocationSize = 1)
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
